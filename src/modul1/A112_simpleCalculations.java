package modul1;

public class A112_simpleCalculations {
    public static void main(String[] args) {
        int a = 455;
        double b = 1.78;
        System.out.printf("%d + %.2f = %.2f \n", a, b, (a + b));
        System.out.printf("%d - %.2f = %.2f \n", a, b, (a - b));
        System.out.printf("%d * %.2f = %.1f \n", a, b, (a * b));
        System.out.printf("%d / %.2f = %.2f \n", a, b, (a / b));
    }
}
